#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: the first int
 * @b: the second int
 *
 * Return: the sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: the first int
 * @b: the second int
 *
 * Return: the mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: the first int
 * @b: the second int
 *
 * Return: the div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of division
 * @a: the first int
 * @b: the second int
 *
 * Return: the mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
